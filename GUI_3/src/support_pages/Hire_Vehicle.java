    private void cmboMakeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmboMakeActionPerformed
        cmboMake.addActionListener (new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                
                cmboMake = (JComboBox<String>) e.getSource();

                Object selected = cmboMake.getSelectedItem();             
                                
                if (null != selected.toString())
                    switch (selected.toString()) {
                    case "Honda":                        
                        cmboModel.removeAllItems();
                for (String Honda1 : Honda) {
                    cmboModel.addItem(Honda1);
                }
                        break;
                    case "Ford":
                        cmboModel.removeAllItems();
                for (String Ford1 : Ford) {
                    cmboModel.addItem(Ford1);
                }
                        break;
                    case "Volkswagen":
                        cmboModel.removeAllItems();
                for (String VW1 : VW) {
                    cmboModel.addItem(VW1);
                }
                        break;
                    case "BMW":
                        cmboModel.removeAllItems();
                for (String BMW1 : BMW) {
                    cmboModel.addItem(BMW1);
                }
                        break;
                    default:
                        cmboModel.removeAllItems();
                        break;
                }
            }
        });                
    }//GEN-LAST:event_cmboMakeActionPerformed

