    private boolean isSaveable() {
        Boolean blnValid = false;
        try
        {
            if (cmboMake.getSelectedItem().toString().length() <= 1 && cmboMake.getSelectedItem().toString().length() <= 1 && cmboStaff.getSelectedItem().toString().length() <= 1 )
            {
                blnValid = false;
            }
        } catch (NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(this, "The provided data is invalid", "Error reading data", JOptionPane.ERROR_MESSAGE);
        }
        return blnValid;
    }      
